    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        int checkMedicineExist = 0;
        String uniqueId =medicineidTxt.getText();
        if(uniqueId.equals("")){
            JOptionPane.showMessageDialog(null,"Medicine Id field is required.");
        }else{
            try {
                Connection con=ConnectionProvider.getConnection();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("SELECT * FROM medicine WHERE uniqueId='"+uniqueId+"'");
                while(rs.next()){
                    medicineidTxt.setEditable(false);
                    nameTxt.setText(rs.getString("name"));
                    companynameTxt.setText(rs.getString("companyName"));
                    quantityTxt.setText(rs.getString("quantity"));
                    priceTxt.setText(rs.getString("price"));
                    manuDate.setDate(rs.getDate("manufacturedDate"));
                    expDate.setDate(rs.getDate("expiryDate"));
                    quantityTxt.setEditable(false);
                    checkMedicineExist=1;
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
            if(checkMedicineExist==0){
                JOptionPane.showMessageDialog(null,"Medicine Id does not exist");
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

