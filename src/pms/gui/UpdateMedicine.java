    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String uniqueId=medicineidTxt.getText();
        String name=nameTxt.getText();
        String companyname=companynameTxt.getText();
        String quantity=quantityTxt.getText();
        String price=priceTxt.getText();
        Date mDate=manuDate.getDate();
        Date eDate=expDate.getDate();
        String addQuantity=addquantityTxt.getText();
        int totalQuantity=0;
        if(addQuantity.equals("")){
            totalQuantity=Integer.parseInt(quantity);
        }else{
            totalQuantity=Integer.parseInt(quantity)+Integer.parseInt(addQuantity);
            System.out.println(""+totalQuantity);
        }
        if(uniqueId.equals("")){
            JOptionPane.showMessageDialog(null,"Medicine Id field is required.");
        }else if(name.equals("")){
            JOptionPane.showMessageDialog(null,"Name field is required.");
        }else if(companyname.equals("")){
            JOptionPane.showMessageDialog(null,"Company Name field is required.");
        }else if(!addQuantity.matches(numberPattern)){
            JOptionPane.showMessageDialog(null,"Add Quantity field is invalid.");
        }else if(price.equals("")){
            JOptionPane.showMessageDialog(null,"Price field is required.");
        }else if(!price.matches(numberPattern)){
            JOptionPane.showMessageDialog(null,"Price field is invalid.");
        }else if(mDate.equals("")){
            JOptionPane.showMessageDialog(null,"Manufactured Date field is required.");
        }else if(eDate.equals("")){
            JOptionPane.showMessageDialog(null,"Expiiry Date field is required.");
        }else{
            try {
                Connection con=ConnectionProvider.getConnection();
                PreparedStatement ps=con.prepareStatement("UPDATE medicine SET name=?,companyName=?,quantity=?,price=?,manufacturedDate=?,expiryDate=? WHERE uniqueId=?");
                ps.setString(1,name);
                ps.setString(2,companyname);
                ps.setString(3,""+totalQuantity);
                ps.setString(4, price);
                ps.setDate(5,new java.sql.Date(mDate.getTime()));
                ps.setDate(6,new java.sql.Date(eDate.getTime()));
                ps.setString(7,uniqueId);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Medicine updated successfully");
                setVisible(false);
                new UpdateMedicine().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

